void OLED_Data_Process(uint8_t Level, uint8_t Lock, uint8_t Err, uint8_t Connect)
{
    unsigned char i, j;

    for(j = 0; j < 10; j++)
    {
        for(i = 0; i < 128; i++)
        {
            tab_1[j][i] = 0x00;
        }
    }

    if(Err == 0)
    {
        if(Connect == 0)
        {

            if(Level == 31)
            {
                for(j = 0; j < 10; j++)
                {
                    for(i = 0; i < 32; i++)
                    {
                        tab_1[j][128 - i] = 0x00;
                    }
                }

                for(j = 0; j < 10; j++)
                {
                    for(i = 0; i < 32; i++)
                    {
                        tab_1[j][128 - 40 - i] = 0x00;
                    }
                }
            }
            else
            {
                Display_Level(Level);
            }

            if((Lock == 1) && (Lock_Blink_Time >= 800))
            {
                for(j = 0; j < 5; j++)
                {
                    for(i = 0; i < 20; i++)
                    {
                        tab_1[j][128 - 40 - 32 - 7 - 30 - i] = Lock_Data[j][i];
                    }
                }
            }
            else
            {
                for(j = 0; j < 5; j++)
                {
                    for(i = 0; i < 20; i++)
                    {
                        tab_1[j][128 - 40 - 32 - 7 - 30 - i] = 0x00;
                    }
                }
            }

            for(j = 0; j < 5; j++)
            {
                for(i = 0; i < 50; i++)
                {
                    tab_1[j + 5][128 - 40 - 32 - 7 - i] = Stength[j][i];
                }
            }
        }
        else
        {
            for(j = 0; j < 10; j++)
            {
                for(i = 0; i < 128; i++)
                {
                    tab_1[j][i] = Connect_data[j][i];
                }
            }
        }
    }
    else
    {
        switch(Err)
        {
            case 1:
                Display_Level(41);
                break;

            case 2:
                Display_Level(42);
                break;

            case 3:
                Display_Level(43);
                break;
        }

        for(j = 0; j < 5; j++)
        {
            for(i = 0; i < 50; i++)
            {
                tab_1[j + 5][128 - 40 - 32 - 7 - i] = Stength[j][i];
            }
        }
    }
}

void OLED_Display_Off(void)
{
    unsigned char page_number, column_number;

    for(page_number = 0; page_number < 10; page_number++)
    {
        OLED_Write_Cmd(0xb0 + page_number);
        OLED_Write_Cmd(0x10);
        OLED_Write_Cmd(0x00);

        for(column_number = 0; column_number <= 128; column_number++)
        {
            OLED_Write_Data(0X00);
        }
    }
}

void OLED_Display(void)
{
    unsigned char page_number, column_number;

    for(page_number = 0; page_number < 10; page_number++)
    {
        OLED_Write_Cmd(0xA1);
        OLED_Write_Cmd(0xC8);
        OLED_Write_Cmd(0xb0 - 1 + page_number);
        OLED_Write_Cmd(0x10);
        OLED_Write_Cmd(0x00);

        for(column_number = 0; column_number <= 128; column_number++)
        {
            OLED_Write_Data(tab_1[page_number][column_number]);
        }
    }
}

void OLED_Init(void)
{
    uint16_t time;
    OLED_CS_LOW;
    OLED_RST_HIGH;
    time = 5000;

    while(time--);

    OLED_RST_LOW;
    time = 10000;

    while(time--);

    OLED_RST_HIGH;
    time = 10000;

    while(time--);

    time = 10000;

    while(time--);
	
	OLED_Write_Cmd(0xAE);     //Set Display Off 

	OLED_Write_Cmd(0xd5);     //display divide ratio/osc. freq. mode	
	OLED_Write_Cmd(0x51);     //

	OLED_Write_Cmd(0xA8);     //multiplex ration mode: 
	OLED_Write_Cmd(0x1F);

	OLED_Write_Cmd(0xD3);     //Set Display Offset   
	OLED_Write_Cmd(0x10);

	OLED_Write_Cmd(0x40);     //Set Display Start Line 

	OLED_Write_Cmd(0xAD);     //DC-DC Control Mode Set 
	OLED_Write_Cmd(0x8B);     //DC-DC ON/OFF Mode Set 

	OLED_Write_Cmd(0x31);     //Set Pump voltage value 

	OLED_Write_Cmd(0xA1);     //Segment Remap	 

	OLED_Write_Cmd(0xC8);     //Sst COM Output Scan Direction	

	OLED_Write_Cmd(0xDA);     //common pads hardware: alternative	
	OLED_Write_Cmd(0x12);

	OLED_Write_Cmd(0x81);     //contrast control 
	OLED_Write_Cmd(0xa0);		//

	OLED_Write_Cmd(0xD9);	    //set pre-charge period	  
	OLED_Write_Cmd(0x22);

	OLED_Write_Cmd(0xDB);     //VCOM deselect level mode 
	OLED_Write_Cmd(0x25);	    //

	OLED_Write_Cmd(0xA4);     //Set Entire Display On/Off	

	OLED_Write_Cmd(0xA6);     //Set Normal Display 

	OLED_Write_Cmd(0xAF);     //Set Display On 
}






