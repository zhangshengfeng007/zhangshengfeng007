/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM14_Init();
   MX_TIM16_Init();
  /* USER CODE BEGIN 2 */
   sys_info.Max_repeat_times = mag_data_arr[0].set_repeat_times;
   sys_info.cur_fun = mag_data_arr[0].p_fun;
   all_p_port_on();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
	  if(sys_info.N_pin_on_flag)
	  {
		delay_1ms_timer_stop();
		sys_info.N_pin_on_flag = 0;
		Magen_select_n_pins_on();
		  
		sys_info.cur_mag_step ++;
		if(sys_info.cur_mag_step  > sys_info.Max_mag_step)
		{
			sys_info.cur_repeat_cnt ++;
			if(sys_info.cur_repeat_cnt >= sys_info.p_magn_data->set_repeat_times)
			{
				sys_info.cur_muliti_magn_index ++;
				if(sys_info.cur_muliti_magn_index >= sys_info.Max_muliti_magn_num)
				{
					sys_info.cur_muliti_magn_index = 0;
				}
				sys_info.p_magn_data = &mag_data_arr[sys_info.cur_muliti_magn_index];
			
			}
			sys_info.cur_mag_step = MAG_STEP1;
		}
	  }
	  
	  
	if(sys_info.task_1ms_cnt >= 299)
	{
		sys_info.task_1ms_cnt = 0;
		sys_info.p_magn_data->p_fun();
	}

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

